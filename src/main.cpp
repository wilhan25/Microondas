#define led_pin 2

// ── configs do controle ──────────────────────
const uint16_t RECV_PIN = 15;
const uint16_t kCaptureBufferSize = 1024;
const uint8_t kTimeout = 50;
IRrecv controleINFRA(RECV_PIN, kCaptureBufferSize, kTimeout);
decode_results results;

// ── configs do motor ─────────────────────────
#define IN1 14
#define IN2 27
#define IN3 26
#define IN4 25

// O 28BYJ-48 em half-step precisa da ordem exata: IN1, IN3, IN2, IN4
AccelStepper stepper(AccelStepper::HALF4WIRE, IN1, IN3, IN2, IN4);

// velocidade segura para o 28BYJ-48 — acima de 700 ele perde passo
#define MOTOR_VELOCIDADE 600
const int STEPS_PER_REV = 4096;

// ── configs da tela ──────────────────────────
#define SDA_PIN 22
#define SCL_PIN 23
#define Largura_Tela 128
#define Comprimento_Tela 64
Adafruit_SSD1306 display(Largura_Tela, Comprimento_Tela, &Wire, -1);

// ── estado compartilhado entre tasks ─────────
// volatile avisa o compilador que outra task pode mudar esse valor
volatile bool motor_ligado = false;
volatile uint32_t tempo_restante_ms = 0; // tempo que ainda falta girar

void motorDesligar()
{
  digitalWrite(IN1, LOW);
  digitalWrite(IN2, LOW);
  digitalWrite(IN3, LOW);
  digitalWrite(IN4, LOW);
}

// ── Task do controle IR ──────────────────────
void TaskControle(void *pv)
{
  while (1)
  {
    if (controleINFRA.decode(&results))
    {
      uint32_t linha = results.value;

      if (linha == 0xFFFFFFFF)
      { // ignora repeat NEC
        controleINFRA.resume();
        vTaskDelay(pdMS_TO_TICKS(10));
        continue;
      }

      botoes_controle btn = conversaoBtn(linha);
      // substitua por essa
      Serial.printf("[IR] 0x%08X\n", linha);
      switch (btn)
      {
      case BTN_PLAY:
        // liga o motor por 3 segundos
        motor_ligado = true;
        tempo_restante_ms = 3000;
        Serial.println("Motor: ligado por 3s");
        break;

      case BTN_PLUS:
        // adiciona 30 segundos ao tempo restante
        tempo_restante_ms += 30000;
        motor_ligado = true;
        Serial.printf("Motor: +30s (total %lums)\n", tempo_restante_ms);
        break;

      case BTN_MINUS:
        // remove 30 segundos, mínimo zero
        if (tempo_restante_ms > 30000)
          tempo_restante_ms -= 30000;
        else
        {
          tempo_restante_ms = 0;
          motor_ligado = false;
        }
        Serial.printf("Motor: -30s (total %lums)\n", tempo_restante_ms);
        break;

      case BTN_POWER:
        // para tudo imediatamente
        motor_ligado = false;
        tempo_restante_ms = 0;
        motorDesligar();
        Serial.println("Motor: parado");
        break;

      case BTN_UNKNOWN:
        Serial.printf("Botão não mapeado: 0x%08X\n", linha);
        break;

      default:
        break;
      }

      controleINFRA.resume();
    }
    vTaskDelay(pdMS_TO_TICKS(10));
  }
}

// ── Task do motor ────────────────────────────
void TaskMotor(void *pv)
{
  stepper.setMaxSpeed(MOTOR_VELOCIDADE);
  stepper.setSpeed(MOTOR_VELOCIDADE);

  while (1)
  {
    if (motor_ligado && tempo_restante_ms > 0)
    {
      stepper.runSpeed();     // executa um passo se for a hora
      tempo_restante_ms -= 1; // desconta 1ms por iteração
      digitalWrite(led_pin, HIGH);

      if (tempo_restante_ms == 0)
      {
        motor_ligado = false;
        motorDesligar(); // desliga bobinas ao terminar
        Serial.println("Motor: tempo esgotado");
      }
    }
    else
    {
      digitalWrite(led_pin, LOW);
      motorDesligar(); // garante bobinas desligadas quando parado
    }

    vTaskDelay(pdMS_TO_TICKS(1)); // loop a cada 1ms
  }
}

// ── Task da tela ─────────────────────────────
void TaskTela(void *pv)
{
  // tela de abertura: bitmap do microondas
  display.clearDisplay();
  display.drawBitmap(0, 0, epd_bitmap_MircoondasPixel, 128, 64, SSD1306_WHITE);
  display.display();

  while (1)
  {
    if (motor_ligado)
    {
      // mostra tempo restante enquanto gira
      display.setTextSize(1);
      display.setTextColor(SSD1306_WHITE);
      display.setCursor(20, 25);
      display.println("GIRANDO...");
    }
    else
    {
      display.clearDisplay();
      // tela parada: mostra bitmap
      display.drawBitmap(0, 0, epd_bitmap_MircoondasPixel, 128, 64, SSD1306_WHITE);
    }

    display.display();
    vTaskDelay(pdMS_TO_TICKS(200)); // atualiza a tela 5x por segundo
  }
}

void setup()
{
  Serial.begin(115200);
  Wire.begin(SDA_PIN, SCL_PIN);

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    Serial.println("Falha ao iniciar OLED");
    while (1)
      ;
  }
  display.clearDisplay();
  display.display();
  delay(1000);

  controleINFRA.enableIRIn();
  pinMode(led_pin, OUTPUT);

  // pinos do motor como saída
  pinMode(IN1, OUTPUT);
  pinMode(IN2, OUTPUT);
  pinMode(IN3, OUTPUT);
  pinMode(IN4, OUTPUT);
  motorDesligar(); // garante bobinas desligadas na inicialização

  // cada task em um core diferente para não brigarem
  xTaskCreatePinnedToCore(TaskControle, "controle", 4096, NULL, 2, NULL, 0);
  xTaskCreatePinnedToCore(TaskMotor, "motor", 4096, NULL, 2, NULL, 0);
  xTaskCreatePinnedToCore(TaskTela, "tela", 4096, NULL, 1, NULL, 1);
}

void loop() {}