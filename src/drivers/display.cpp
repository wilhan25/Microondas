bool display_init() {
    Wire.begin(PIN_SDA, PIN_SCL);
    if (!oled.begin(SSD1306_SWITCHCAPVCC, DISPLAY_ENDERECO)) {
        Serial.println("[display] falha ao iniciar OLED");
        return false;
    }
    oled.clearDisplay();
    oled.display();

    // splash screen
    oled.drawBitmap(0, 0, bitmap_microondas, 128, 64, SSD1306_WHITE);
    oled.display();
    delay(2000);

    return true;
}

void display_fundo() {
    oled.clearDisplay();
    oled.drawBitmap(0, 0, bitmap_microondas, 128, 64, SSD1306_WHITE);
}

void display_relogio(uint8_t h, uint8_t m, uint8_t s) {
    oled.setTextColor(SSD1306_WHITE);

    // HH:MM em tamanho 2
    oled.setTextSize(2);
    oled.setCursor(AREA_X + 10, AREA_Y + 2);
    oled.printf("%02d:%02d", h, m);

    // segundos em tamanho 1
    oled.setTextSize(1);
    oled.setCursor(AREA_X + 57, AREA_Y + 28);
    oled.printf(":%02d", s);
}

void display_digitando(const uint8_t digitos[4]) {
    oled.setTextColor(SSD1306_WHITE);

    oled.setTextSize(2);
    oled.setCursor(AREA_X + 10, AREA_Y + 4);
    oled.printf("%d%d:%d%d", digitos[0], digitos[1], digitos[2], digitos[3]);

    oled.setTextSize(1);
    oled.setCursor(AREA_X + 16, AREA_Y + 28);
    oled.print("PLAY p/ iniciar");
}

void display_aquecendo(uint32_t tempo_ms) {
    oled.setTextColor(SSD1306_WHITE);

    oled.setTextSize(2);
    oled.setCursor(AREA_X + 10, AREA_Y + 4);
    oled.printf("%02lu:%02lu",
        (tempo_ms / 1000) / 60,
        (tempo_ms / 1000) % 60);

    oled.setTextSize(1);
    oled.setCursor(AREA_X + 13, AREA_Y + 28);
    oled.print("AQUECENDO");
}

void display_atualizar() {
    oled.display();
}
